                            Though the Language Commission has clearly stated Maithili as the leading language of the
                            province, the reference to the contact language has raised questions about its future.
                            The discourse of giving priority to Hindi as lingua franca and Magahi as mother tongue has
                            raised question marks on the future of Maithili language. </p>

                        <p class="fs-5 mb-4">However, as Maithili is not listed as an endangered language in the world
                            famous reference book ‘South Asian and Middle East’, we can be sure that it is safe at
                            present.</p>

                        <p class="fs-5 mb-4">Similarly, the book mentions Nepali, Maithili and Bhojpuri as safe
                            languages However, careful analysis indicates that the language may not possibly survive in
                            the future. Some have started mentioning the accidental factors behind the death of Maithili
                            language. Historically, the Maithili language has spread to the Kathmandu Valley and Palpa
                            and is not limited to the Mithila region.
                            Language has its own script, seeking a special place in literature. It has been found that
                            the kings of the Sen dynasty also used Maithili as their official language Given the
                            advanced status of this language, literature, culture, and art, the melody of the language,
                            and the beauty of expression and flexibility, the Malla kings of the Kathmandu Valley have
                            used Maithili in their plays, poems and songs. The language demonstrates the ancient dignity
                            of Nepal.</p>

                        <p class="fs-5 mb-4">The language of the Mithila region is Maithili. Scholars claim that
                            Maithili is the mother tongue of ancient Mithila. According to the 18th verse of the Sundar
                            Kand of the Balmiki Ramayana, when Ravana had kept Sita captive in the Ashoka garden,
                            Hanuman spoke to her in Maithili.
                            According to Dr. Durgananda Jha’s book titled ‘History of Maithili Literature’, Mithila has
                            existed since the Vedic era The environment, flora and wildlife described in Vedas, Puranas,
                            Upanishads and Mimamas are in harmony with Mithila After the rise of the Janaka dynasty, the
                            kingdom of Mithila was officially established, and its capital was Janakpurdham Historians
                            date the Janaka dynasty from 3000 to 600 BC. Sita was born in the middle of the period.
                            Situated between the rivers Koshi, Kamala, Bagmati, Gandaki and Ganga, this land is said to
                            be surrounded in three directions
                            The Siddha scriptures written during the eighth century are in Maithili language Dohas and
                            songs of the sage poets of that time like Dak, Bhas and Bhusuppa are still available. Most
                            of the works of literary figures are available in the Terai of Nepal. Jyotiswara's 'Dhurat
                            Samagama', the oldest ancient scripture, mentions Maithili as a language of the
                            Indo-European language family.
                            According to linguist Dr Ramawatar Yadav, out of 150 manuscripts of plays written between
                            900 AD and 18th century in the National Archives of Nepal, 115 are in Maithili, 26 in Newari
                            and five in Bengali So far, as confirmed, Maithili is the oldest language behind Marathi
                            among the 28 languages in the Indo-Aryan community.
                            A survey conducted by George Abraham Grierson in the 19th century mentions that Maithili had
                            an independent literary tradition in the Terai and most of Bihar in India </p>

                        <p class="fs-5 mb-4">According to the research done by historian Dr. Tulasiram Vaidhya at
                            Simrongarh, Maithili language got state protection during the reign of the kings of the
                            Karnataka dynasty
                            From the early 14th century, the language had a widespread influence in places including
                            Kantipur, Lalitpur, Bhaktapur and Banepa. According to History Award winner Baburam Acharya,
                            the Sen kings used Maithili as their official language.
                            The great poet of Maithili language Vidyapati was born already during the fourth century
                            Born around 1340, Vidyapati wrote more than a dozen poems and prose. Even Rabindranath
                            Tagore accepted Vidyapati as his poetic guru.</p>

                        <p class="fs-5 mb-4">During his stay in Banauli village near Janakpurdham, Vidyapati translated
                            The Bhagavad Gita into Maithili, and wrote half a dozen books, according to extant evidence
                            Maithili is found to be used as the official language in the Kathmandu Valley before Prithvi
                            Narayan Shah conquered the valley The Malla kings spoke in Maithili, and wrote literature in
                            the language It seemed that Prithvi Narayan banned the Maithili language and culture, and
                            recognized Nepali as the official language
                            According to history, King Mahendra in the name of ‘One King, One Nation, One Language and
                            Single National Costume’ later attacked the language and Maithili culture and tried to
                            eliminate the identity of Maithili language</p>


